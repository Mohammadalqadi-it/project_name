void main() {
  // [+] operation
  print(5 + 5);

  // [*] operation
  print(5 * 5);

  // [/] operaton
  print(5 / 5);
  // [%] (mod) operation
  print(10 % 9);

  //[String] datatype
  String myStringForSomething = 'string datatype';

  // [int] datatype
  int intNumber = 5;

  // [double] datatype
  double doubleNumber = 5.5;

  // [num] datatype: int can not containe [double], and double can not contain [int]
  num numForAll = 5;

  // [num] can contain [double] or [int]
  num numForAll2 = 5.5;

  //[bool] datatype
  bool flag = true;
}
