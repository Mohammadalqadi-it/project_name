void main() {
  int mark = 51;

  switch (mark) {
    case < 50:
      print("Fail");
      break;
    case >= 50 && < 60:
      print("D");
      break;
    case > 1:
      print("C");
      break;
    case >= 70 && < 80:
      print("B");
      break;
    case >= 80 && < 90:
      print("A");
      break;
    case > 90:
      print("A+");
      break;
    default:
  }
}

